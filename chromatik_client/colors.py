def toHSB(c):
	r = (c >> 16) & 0xff
	g = (c >> 8) & 0xff
	b = c & 0xff

	ma = max(r, g, b)
	mi = min(r, g, b)

	if ma == mi:
		hue = 0
	elif ma == r:
		hue = (g - b) / (ma - mi)
	elif ma == g:
		hue = (b - r) / (ma - mi) + 2
	else:
		hue = (r - g) / (ma - mi) + 4
	hue *= 256 / 6
	if hue < 0:
		hue += 256
	hue = int(hue);

	if ma != 0:
		sat = (ma - mi) / ma * 255
	else:
		sat = 0

	brightness = ma

	return (hue, sat, brightness)


